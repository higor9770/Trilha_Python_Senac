class Professor:
    def __init__(self, cpf = None, nome = None, telefone = None, email = None, data_nascimento = None, sexo = None):
        self.__cpf = cpf
        self.__nome = nome
        self.__telefone = telefone
        self.__email = email
        self.__data_nascimento = data_nascimento
        self.__sexo = sexo

    def setcpf(self, cpf):
        self._cpf = cpf
    def getcpf(self):
        return self.__cpf

    def setnome(self, nome):
        self.__nome = nome
    def getnome(self):
        return self.__nome

    def settelefone(self, telefone):
        self.__telefone = telefone
    def gettelefone(self):
        return self.__telefone

    def setemail(self, email):
        self.__email = email
    def getemail(self):
        return self.__email
    
    def setdata_nascimento(self, data_nascimento):
        self.__data_nascimento = data_nascimento
    def getdata_nascimento(self):
        return self.__data_nascimento
    
    def setsexo(self, sexo):
        self.__sexo = sexo
    def getsexo(self):
        return self.__sexo
    
